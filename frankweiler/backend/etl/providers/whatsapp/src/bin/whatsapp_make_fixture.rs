async fn insert_jids(pool: &SqlitePool, jids: &[JidSpec]) -> Result<()> {
    for j in jids {
        sqlx::query("INSERT INTO jid (_id, user, server, raw_string) VALUES (?, ?, ?, ?)")
            .bind(j.id)
            .bind(&j.user)
            .bind(&j.server)
            .bind(&j.raw_string)
            .execute(pool)
            .await
            .context("insert jid")?;
    }
    Ok(())
}

async fn insert_chats(pool: &SqlitePool, chats: &[ChatSpec]) -> Result<()> {
    for c in chats {
        sqlx::query("INSERT INTO chat (_id, jid_row_id, subject, group_type) VALUES (?, ?, ?, ?)")
            .bind(c.id)
            .bind(c.jid_row_id)
            .bind(&c.subject)
            .bind(c.group_type)
            .execute(pool)
            .await
            .context("insert chat")?;
    }
    Ok(())
}

async fn insert_messages(pool: &SqlitePool, messages: &[MessageSpec]) -> Result<HashMap<i64, i64>> {
    let mut map = HashMap::with_capacity(messages.len());
    for m in messages {
        let row = sqlx::query(
            "INSERT INTO message (_id, chat_row_id, from_me, key_id, sender_jid_row_id, \
                timestamp, message_type, text_data, sort_id) \
             VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?) RETURNING _id",
        )
        .bind(m.id)
        .bind(m.chat_row_id)
        .bind(m.from_me)
        .bind(&m.key_id)
        .bind(m.sender_jid_row_id)
        .bind(m.timestamp)
        .bind(m.message_type)
        .bind(&m.text_data)
        .bind(m.id) // sort_id mirrors _id for deterministic ordering
        .fetch_one(pool)
        .await
        .context("insert message")?;
        let id: i64 = row.get("_id");
        map.insert(m.id, id);

        if let Some(media) = &m.media {
            sqlx::query(
                "INSERT INTO message_media (message_row_id, chat_row_id, mime_type, file_path, \
                    file_size, media_caption, width, height) \
                 VALUES (?, ?, ?, ?, ?, ?, ?, ?)",
            )
            .bind(id)
            .bind(m.chat_row_id)
            .bind(&media.mime_type)
            .bind(&media.file_path)
            .bind(media.file_size)
            .bind(&media.media_caption)
            .bind(media.width)
            .bind(media.height)
            .execute(pool)
            .await
            .context("insert message_media")?;
        }
    }
    Ok(map)
}

async fn insert_reactions(
    pool: &SqlitePool,
    reactions: &[ReactionSpec],
    msg_id_to_pk: &HashMap<i64, i64>,
) -> Result<()> {
    // Each reaction is one row in `message_add_on` (carrying who reacted
    // and to what) plus one row in `message_add_on_reaction` (carrying
    // the emoji). The pair shares the autoincrement `_id` from
    // `message_add_on` as their join key.
    for (idx, r) in reactions.iter().enumerate() {
        let parent_pk = msg_id_to_pk
            .get(&r.parent_message_id)
            .copied()
            .ok_or_else(|| anyhow!("reaction parent message {} not found", r.parent_message_id))?;
        // Look up parent's chat_row_id to satisfy the add_on row's own
        // chat_row_id column.
        let parent_chat_row_id: i64 =
            sqlx::query_scalar("SELECT chat_row_id FROM message WHERE _id = ?")
                .bind(parent_pk)
                .fetch_one(pool)
                .await
                .context("lookup parent chat_row_id")?;
        let addon_id = 1000 + idx as i64;
        sqlx::query(
            "INSERT INTO message_add_on (_id, chat_row_id, from_me, key_id, sender_jid_row_id, \
                parent_message_row_id, timestamp, message_add_on_type) \
             VALUES (?, ?, ?, ?, ?, ?, ?, 2)",
        )
        .bind(addon_id)
        .bind(parent_chat_row_id)
        .bind(r.from_me)
        .bind(&r.key_id)
        .bind(r.sender_jid_row_id)
        .bind(parent_pk)
        .bind(r.timestamp)
        .execute(pool)
        .await
        .context("insert message_add_on")?;
        sqlx::query(
            "INSERT INTO message_add_on_reaction (message_add_on_row_id, reaction, sender_timestamp) \
             VALUES (?, ?, ?)",
        )
        .bind(addon_id)
        .bind(&r.reaction)
        .bind(r.timestamp)
        .execute(pool)
        .await
        .context("insert message_add_on_reaction")?;
    }
    Ok(())
}

fn decode_hex32(s: &str) -> Result<[u8; 32]> {
    if s.len() != 64 {
        anyhow::bail!("expected 64 hex chars, got {}", s.len());
    }
    let mut out = [0u8; 32];
    for i in 0..32 {
        out[i] = u8::from_str_radix(&s[i * 2..i * 2 + 2], 16)
            .with_context(|| format!("hex byte at {}", i * 2))?;
    }
    Ok(out)
}

fn decode_hex16(s: &str) -> Result<[u8; 16]> {
    if s.len() != 32 {
        anyhow::bail!("expected 32 hex chars, got {}", s.len());
    }
    let mut out = [0u8; 16];
    for i in 0..16 {
        out[i] = u8::from_str_radix(&s[i * 2..i * 2 + 2], 16)
            .with_context(|| format!("hex byte at {}", i * 2))?;
    }
    Ok(out)
}
